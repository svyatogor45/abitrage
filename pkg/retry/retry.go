package retry

// retry.go - механизм повторных попыток
//
// Назначение:
// Retry logic с экспоненциальным backoff для надежности.
//
// Функции:
// - Do: выполнить операцию с повторами
//   * Экспоненциальная задержка: 2s, 4s, 8s, 16s
//   * Максимум N попыток
//   * Возврат последней ошибки если все попытки неудачны
// - DoWithContext: с поддержкой context для отмены
//
// Конфигурация:
// - MaxRetries: максимум попыток
// - InitialDelay: начальная задержка
// - MaxDelay: максимальная задержка
// - Multiplier: множитель для экспоненты
//
// Использование:
// - Повтор запросов к API бирж при сетевых ошибках
// - Повтор операций с БД при временных сбоях
