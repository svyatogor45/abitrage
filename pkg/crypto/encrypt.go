package crypto

// encrypt.go - шифрование и дешифрование данных
//
// Назначение:
// Защита чувствительных данных (API ключей) с помощью AES-256 шифрования.
//
// Функции:
// - Encrypt: шифровать plaintext в ciphertext
//   * AES-256-GCM encryption
//   * Возврат base64 encoded строки
//   * Использует ключ из config (32 байта)
// - Decrypt: расшифровать ciphertext в plaintext
//   * Декодирование base64
//   * AES-256-GCM decryption
// - GenerateKey: генерация случайного ключа (32 байта)
//
// Использование:
// - Шифрование API ключей перед сохранением в БД
// - Расшифровка при извлечении из БД для использования
//
