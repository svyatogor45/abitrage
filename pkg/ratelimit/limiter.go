package ratelimit

// limiter.go - контроль частоты запросов
//
// Назначение:
// Rate limiting для соблюдения ограничений API бирж.
//
// Функции:
// - NewRateLimiter: создать rate limiter
//   * requestsPerSecond: лимит запросов в секунду
// - Wait: блокирующее ожидание разрешения на запрос
//   * Используется перед каждым API вызовом
// - Allow: проверка без блокировки
//   * Возвращает true если запрос можно выполнить сейчас
//
// Алгоритм:
// - Token Bucket: пополняемое ведро токенов
// - Leaky Bucket: утекающее ведро
//
// Использование:
// - Отдельный limiter для каждой биржи
// - Конфигурация согласно документации биржи
// - Предотвращение превышения rate limits
//
