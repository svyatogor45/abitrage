// spread.go - расчет спреда между биржами
//
// Назначение:
// Вычисляет ценовые расхождения между биржами с учетом всех факторов.
//
// Функции:
// - Получение текущих цен со всех подключенных бирж
// - Анализ стакана ордеров (orderbook depth) для расчета средневзвешенной цены
// - Моделирование исполнения ордера заданного объема
// - Расчет базового спреда: (P_high - P_low) / P_low × 100
// - Учет комиссий тейкера на обеих биржах
// - Расчет чистого спреда (net spread) после комиссий
// - Проверка достаточности ликвидности для заданного объема
// - Кэширование данных стаканов для минимизации задержек
//
